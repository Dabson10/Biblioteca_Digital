    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        try {
            PreparedStatement ps = null;
            ResultSet rs = null;

            ConexionBd.Conexion objetoConexion = new ConexionBd.Conexion();

            String consulta = "SELECT correo,clave FROM biblio_usuarios.registro WHERE correo=? AND clave =?; ";
            ps = objetoConexion.establecerConexion().prepareStatement(consulta);
            String correo = txtCorreo.getText();
            String clave = String.valueOf(pswContrasenia.getPassword());
            ps.setString(1, correo);
            ps.setString(2, clave);
            rs = ps.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "Correo correcto");
                Paneles.NewLibros panel = new Paneles.NewLibros();

                panel.setSize(818, 410);
                panel.setLocation(0, 0);

                panelSesion.removeAll();
                panelSesion.add(panel, BorderLayout.CENTER);
                panelSesion.revalidate();
                panelSesion.repaint();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se conecto");
        }

    }//GEN-LAST:event_btnGuardarActionPerformed


